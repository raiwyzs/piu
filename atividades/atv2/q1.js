// Verifica se 'n1' é maior que zero
if (n1 > 0) {
  // Se for maior que zero, imprime 'positivo' no console
  console.log("positivo");
// Se não for maior que zero, verifica se é menor que zero
} else if (n1 < 0) {
  // Se for menor que zero, imprime 'negativo' no console
  console.log("negativo");
// Se não entrar nas condições acima, significa que é zero
} else {
  // Imprime 'zero' no console
  console.log("zero");
}